package utils

// find the elements which appear in slice1, but not in slice2
func NotIn(slice1 []int64, slice2 []int64) []int64 {
	var diff []int64

	for _, s1 := range slice1 {
		found := false
		for _, s2 := range slice2 {
			if s1 == s2 {
				found = true
				break
			}
		}
		// int64 not found. We add it to return slice
		if !found {
			diff = append(diff, s1)
		}
	}
	return diff
}
